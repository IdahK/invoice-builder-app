// Main PDF generation function
export const generateInvoicePDF = async (invoice: Invoice): Promise<Blob> => {
  if (pdfMode === 'server') {
    return generateServerPDF(invoice);
  }
  return generateClientPDF(invoice);
};

// Download PDF
export const downloadInvoicePDF = async (invoice: Invoice, filename?: string) => {
  const blob = await generateInvoicePDF(invoice);
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename || `${invoice.invoiceNumber}.pdf`;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
};
